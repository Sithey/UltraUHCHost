public class ThreeArrows extends CustomScenario implements Listener {

    @Override
    public String getName() {
        return "3x Arrows";
    }


    @Override
    public ItemStack getItem() {
        List<String> textes = new ArrayList<>();
        textes.add("§8§m--------------------------------------");
        textes.add("");
        StringBuilder newmsg = new StringBuilder(ChatColor.GRAY + "");
        int i = 0;
        for(String bout : Scenario.THREEARROWS.getDescription().split(" ")){
            newmsg.append(bout).append(" ");
            if(i == 8){
                textes.add(newmsg.toString());
                newmsg = new StringBuilder(ChatColor.GRAY + "");
                i = 0;
            }
            i++;
        }
        if(!newmsg.toString().equals(ChatColor.GRAY + "")){
            textes.add(newmsg.toString());
        }
        textes.add("");
        textes.add("§8§m--------------------------------------");
        return new ItemCreator(Material.ARROW).setName(getName() + " : " + Scenario.THREEARROWS.isEnabled()).setAmount(Scenario.THREEARROWS.isEnabled() ? 1 : 0).setLores(textes).getItem();
    }

    @Override
    public void onStart() {
        Bukkit.getPluginManager().registerEvents(this, Main.getInstance());
    }

    @Override
    public void scatterPlayer(Player player) {

    }

    @Override
    public void onPvP() {

    }

    @Override
    public void onMeetup() {

    }

    @Override
    public void onDeath(Player player, Player killer) {

    }

    @Override
    public String getPath() {
        return "3xarrows";
    }

    @EventHandler (priority = EventPriority.HIGHEST)
    public void onBow(EntityShootBowEvent event){
        if (event.getEntity() instanceof Player){
            new BukkitRunnable() {
                int i = 1;
                @Override
                public void run() {
                    if (i == 3){
                        cancel();
                        return;
                    }
                    for (Player p : Bukkit.getOnlinePlayers())
                    p.playSound(event.getEntity().getLocation(), Sound.SHOOT_ARROW, 2f, 2f);
                    event.getEntity().launchProjectile(Arrow.class);
                    i++;
                }
            }.runTaskTimer(Main.getInstance(), 10, 10);
        }
    }

}
